public class EscapeSequII {
    public static void main(String [] args) {
        System.out.println("Cost\t\t\t" + "Quantity\t\t\t" + "Total" );
        System.out.println("$1,000.00\t\t\t" + "4\t\t\t" + "$4,000.00" );
        System.out.println("$\t50.00" + "\t\t\t8\t\t\t\t" + "$400.00");
        System.out.println("");
        System.out.println("_____________________________________________________");
        System.out.println("TOTAL:\t\t\t" + "\t12\t\t\t" + "$4,400.00");
    }
}
